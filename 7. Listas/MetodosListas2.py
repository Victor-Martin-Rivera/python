lista = [5 , 3 , 1 , 4 , 2]

#Cuenta la cantidad de veces que aparece un elemento en la lista
print(lista.count(5))

#index retorna la posicion en la que se encuentra un dato en la lista
print(lista.index(4))

#Este metodo ordena la lista de forma ascendente
lista.sort()
print(lista)

#Este metodo imprime la lista de forma descendente
lista.reverse()
print(lista)