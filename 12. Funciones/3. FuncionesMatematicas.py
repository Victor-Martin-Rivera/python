import math
import random

#Saca exponenciacion (10^2)
print(math.pow(10, 2))
#Saca la raiz cuadrada de un numero (64)
print(math.isqrt(64))
#Saca el seno de un numero (90)
print(math.sin(90))
#Saca el coseno de un numero (90)
print(math.cos(90))
#Saca la tangente de un numero (90)
print(math.tan(90))
#Saca el factorial de un numero
print(math.factorial(10))
